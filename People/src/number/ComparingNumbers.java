package number;

public class ComparingNumbers {
    public static void main(String[] args) {
        int num1 = 3;
        int num2 = 3;
        System.out.println("Are numbers equal? " + (num1 == num2));
        System.out.println("Is number one higher then number two? " + (num1 > num2));
        System.out.println("Is number one higher then or equal as number two? " + (num1 >= num2));
        System.out.println("Are number not equal? " + (num1 != num2));
    }
}
